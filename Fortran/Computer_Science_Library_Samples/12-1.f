C**********************************************************************
C 12.1　オイラー法による1階微分方程式の初期値問題
C**********************************************************************
      implicit none
      integer,parameter :: NM=20
      real :: f,a,b,c,h,exact
      real :: x(0:NM),y(0:NM)
      integer :: n,j

C アルゴリズム手順１．
      a=0.0
      b=2.0
      c=0.5
      n=20

C アルゴリズム手順２．
      h=(b-a)/real(n)
      x(0)=a
      y(0)=c

C アルゴリズム手順３．
      do j=0,n
        x(j+1)=x(j)+h
        y(j+1)=y(j)+h*f(x(j),y(j))
      end do

C 結果の出力
  900 format(A6, 2A15)
  901 format(f6.2, 2f15.6)
      write(*,900) "x(j)", "calculated", "exact"
      do j=0,n
        write(*,901) x(j),y(j),exact(x(j))
      end do
      end

C**********************************************************************
C 関数の定義
C**********************************************************************
      real function f(x,y)
      real :: x,y

      f=(x**2+x+1.0)-(2.0*x+1.0)*y+y**2
      end function
C**********************************************************************
C 厳密解を計算するための関数
C**********************************************************************
      real function exact(x)
      real :: x

      exact=(x*exp(x)+x+1.0)/(exp(x)+1.0)
      end function
