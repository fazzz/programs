        FORMA='UNFORMATTED'
        IF (NXTWD.EQ.'OVER') FORMA='OVERWRITE  '
        CALL OPENWT(IOUT60,FORMA,NAME,IOUT06)
        WRITE(IOUT06,7006) NXTWD,NAME
