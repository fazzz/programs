CKM   WRITE(IOUT56) IFRMX,FLFRQ
CKM   WRITE(IOUT56) IFRMX,((FLFRQ(I,J),I=1,NUMRES),J=1,15)
C
        REWIND IN47
        CLOSE (IOUT56)
        GOTO 999
      END IF
