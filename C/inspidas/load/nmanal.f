      DO 620 I=1,NUMVAR
        WRITE(IOUT54,'(3F10.3)') FLCM(I),FLCMH(I),FLCME(I),FLCMT(I)
 620  CONTINUE
CKM   WRITE(IOUT54) (FLCM(I),I=1,NUMVAR),(FLCMH(I),I=1,NUMVAR),
CKM  &              (FLCME(I),I=1,NUMVAR),(FLCMT(I),I=1,NUMVAR)
CKM   WRITE(IOUT54) FLCM,FLCMH,FLCME,FLCMT
