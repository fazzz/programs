C-----------------------------------------------------------------------
      SUBROUTINE PUTKV(IOUT)
C-----------------------------------------------------------------------
      IMPLICIT REAL*8(A-H,O-Z)
CMSP  INCLUDE (MAXSIZE)
      INCLUDE '../common/maxsize'
CMSP  INCLUDE (SIZE)
      INCLUDE '../common/size'
CMSP  INCLUDE (EIGEN)
      INCLUDE '../common/eigen'
CMSP  INCLUDE (UNITNUMS)
      INCLUDE '../common/unitnums'
      DIMENSION AKX(MAXVAR),AKY(MAXVAR),AKZ(MAXVAR)
      DIMENSION AKVX(MAXVAR),AKVY(MAXVAR),AKVZ(MAXVAR)
C
      DO 100 IAT=1, NUMATM
C
C READ IN K-MATRIX
C
        READ(IN45) (AKX(I),I=1,NUMVAR),(AKY(I),I=1,NUMVAR),
     &             (AKZ(I),I=1,NUMVAR)
C
C INITIALIZATION
C
      DO 150 NM=1,NUMVAR
        AKVX(NM) = 0.D0
        AKVY(NM) = 0.D0
        AKVZ(NM) = 0.D0
150   CONTINUE
C
C LOOP ON THE MODES
C
      DO 200 NM=1,NUMVAR
C
C LOOP ON THE ANGLES
C
      DO 300 I = 1, NUMVAR
        AKVX(NM) = AKVX(NM) + AKX(I) * EIGVEC(I,NM)
        AKVY(NM) = AKVY(NM) + AKY(I) * EIGVEC(I,NM)
        AKVZ(NM) = AKVZ(NM) + AKZ(I) * EIGVEC(I,NM)
300   CONTINUE
200     CONTINUE
C
C PRINT OUT KV-MATRIX
C
        WRITE(IOUT) (AKVX(NM),NM=1,NUMVAR),(AKVY(NM),NM=1,NUMVAR),
     1              (AKVZ(NM),NM=1,NUMVAR)
100   CONTINUE
C
      REWIND IN45
C
      RETURN
      END
