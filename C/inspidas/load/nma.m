C  NBTYPE:   ATOM TYPE  (ECEPP ORDER)
C
C
C OUTPUT OF CHWAKO
C ATTENTION IIBND2 DOESNOT HAVE THE SAME ARRAY SIZE AS IBND2
      DIMENSION IIBND1(MAXVAR),IIBND2(MAXVAR)
      DIMENSION LUNIT(MAXATM),JBTYPE(MAXATM)
